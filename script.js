function initScrollAnimations() {
    const elements = document.querySelectorAll(
        '.price-card, .service-card, .review-card, .about-content, .contact-grid, .map-wrapper'
    );

    elements.forEach(el => {
        el.classList.add('animate-on-scroll');
    });

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, { threshold: 0.05 });

    elements.forEach(el => observer.observe(el));
}

// ===== REVIEWS CAROUSEL (COVERFLOW) =====
function initReviewsCarousel() {
    const track = document.getElementById('reviewsTrack');
    const prevBtn = document.getElementById('reviewsPrev');
    const nextBtn = document.getElementById('reviewsNext');
    const dotsContainer = document.getElementById('carouselDots');
    const cards = Array.from(track.querySelectorAll('.review-card'));
    const total = cards.length;

    let currentIndex = 0;

    function getPositionClass(index) {
        const diff = index - currentIndex;

        if (diff === 0) return 'active';
        if (diff === -1 || (currentIndex === 0 && index === total - 1)) return 'prev-1';
        if (diff === 1 || (currentIndex === total - 1 && index === 0)) return 'next-1';
        if (diff === -2 || (currentIndex <= 1 && index >= total - 2)) return 'prev-2';
        if (diff === 2 || (currentIndex >= total - 2 && index <= 1)) return 'next-2';
        return 'hidden';
    }

    function updateCarousel() {
        cards.forEach((card, i) => {
            card.className = 'review-card ' + getPositionClass(i);
        });

        dotsContainer.querySelectorAll('.carousel-dot').forEach((dot, i) => {
            dot.classList.toggle('active', i === currentIndex);
        });
    }

    function goToSlide(index) {
        currentIndex = (index + total) % total;
        updateCarousel();
    }

    function createDots() {
        dotsContainer.innerHTML = '';
        for (let i = 0; i < total; i++) {
            const dot = document.createElement('button');
            dot.className = 'carousel-dot';
            dot.addEventListener('click', () => goToSlide(i));
            dotsContainer.appendChild(dot);
        }
    }

    prevBtn.addEventListener('click', () => goToSlide(currentIndex - 1));
    nextBtn.addEventListener('click', () => goToSlide(currentIndex + 1));

    // Keyboard navigation
    document.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowLeft') goToSlide(currentIndex - 1);
        if (e.key === 'ArrowRight') goToSlide(currentIndex + 1);
    });

    createDots();
    updateCarousel();
}

